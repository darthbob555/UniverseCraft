	public int[] getInternal(World world, int x, int y, int z){
		int iron = 0;
		int lapis = 0;
		int redstone = 0; 
		int condensed = 0; 
		int darkMatter = 0; 
		int singularium = 0; 
		int refinedDarkMatter = 0;
		int[] blocks = new int[7];
		for(int x1 = 0; x1<5; x1++){
			for(int y1 = 0; y1<5; y1++){
				for(int z1 = 0; z1<5; z1++){
					Block block = world.getBlock(x+x1, y+y1, z+z1);
					int metadata = world.getBlockMetadata(x+x1, y+y1, z+z1);
					if(block == ModBlocks.metadataBlocks){
						if(metadata == 0) condensed++; 
						else if(metadata == 1) singularium++; 
						else if(metadata == 8) darkMatter++; 
						else if(metadata == 15) refinedDarkMatter++; 
					}
					else if(block == Blocks.iron_block) iron++;
					else if(block == Blocks.lapis_block) lapis++;
					else if(block == Blocks.redstone_block) redstone++;
				}
			}
		}
		blocks[0] = redstone;
		blocks[1] = iron;
		blocks[2] = lapis;
		blocks[3] = condensed;
		blocks[4] = singularium;
		blocks[5] = darkMatter;
		blocks[6] = refinedDarkMatter;
		return blocks;
	}

	public int checkTier(int[] blocks) {
		int tier = 0;
		if(blocks[0] == 12 && blocks[1] >= 9) tier++;
		if(blocks[1] == 21 && blocks[2] >= 9) tier++;
		if(blocks[2] == 21 && blocks[3] >= 9) tier++;
		if(blocks[3] == 21 && blocks[4] >= 9) tier++;
		if(blocks[4] == 21 && blocks[5] >= 9) tier++;
		if(blocks[5] == 21 && blocks[6] == 8) tier++;
		return tier;
	}

